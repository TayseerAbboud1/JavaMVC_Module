// Model class representing the data of a student
public class StudentModel {
    private String name;
    private String id;
    private int score;

    // Getter and setter methods for the model properties

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
